package com.example.chinni.cs5120;

public class PlayerItem {
    String name; // player's name
    int id; // player's id

    public PlayerItem(String name, int id) {
        this.name = name;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
